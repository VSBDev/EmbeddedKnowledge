#!/usr/bin/env node
/**
 * Build the course terminology ledger: one consolidated view of every glossary term across all
 * published lessons, so an authoring agent can read what a term already means before defining it
 * and either adopt that meaning or explicitly protest it.
 *
 * The block review of the first 12 lessons found a whole class of defect the per-lesson review
 * structurally cannot see: the same term (reliability, reproducibility, negative-control,
 * selection-bias) redefined lesson-to-lesson under the same glossary id, with no bridge. Each
 * usage was locally correct, so no single-lesson reviewer could catch it. This ledger makes the
 * cross-lesson view a first-class, machine-readable artifact.
 *
 * A term is DIVERGENT when the same id (or the same normalized term name) carries materially
 * different definitions across lessons and the later lesson does not declare a glossary
 * `alignment` block naming the prior lesson and the relation (adopt / narrow / extend /
 * distinct-sense / supersede). A declared alignment turns a silent redefinition into a documented,
 * reviewer-adjudicable decision — the "adapt or protest" contract.
 *
 * Output: site/data/premed-terminology.json. Regenerated by `npm run site:build`; CI's
 * generated-output check keeps it honest. Non-blocking today (existing lessons predate the
 * contract); `scripts/validate-terminology-ledger.mjs` reports divergences without a declared
 * alignment and can be flipped to blocking once the back-catalogue is normalized.
 */
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const lessonsDir = path.join(root, "lessons");
const outPath = path.join(root, "site", "data", "premed-terminology.json");

// Publication order drives "prior vs later": the merged lesson index carries the canonical order.
const index = JSON.parse(fs.readFileSync(path.join(root, "site", "data", "premed-lessons.json"), "utf8"));
const orderByLesson = new Map();
for (const outcome of index.outcomes || []) {
  for (const lessonId of outcome.publishedLessonIds || []) {
    if (!orderByLesson.has(lessonId)) orderByLesson.set(lessonId, outcome.order ?? 9999);
  }
}
const lessonRank = (id) => (orderByLesson.has(id) ? orderByLesson.get(id) : 9999);

// Normalize definition text for a materiality comparison: lowercase, collapse whitespace, drop
// punctuation.
const normalizeDef = (s) =>
  String(s || "")
    .toLowerCase()
    .replace(/\s+/g, " ")
    .replace(/[.;,]/g, "")
    .trim();

// A crude content-word Jaccard so the ledger can separate a trivial reword (high overlap — the
// same concept said differently, e.g. "outcome") from a materially different sense (low overlap —
// e.g. reliability as measurement consistency vs. evidence credibility). This is a triage signal,
// not a semantic judgement; the authored `alignment` block carries the actual decision.
const STOP = new Set("a an the of to in on and or that this which is are be as by for with its their under over from within into it not no than then when where whose whether some any each all one two".split(" "));
const contentWords = (s) => new Set(normalizeDef(s).split(" ").filter((w) => w.length > 2 && !STOP.has(w)));
const jaccard = (a, b) => {
  const A = contentWords(a);
  const B = contentWords(b);
  if (!A.size && !B.size) return 1;
  let inter = 0;
  for (const w of A) if (B.has(w)) inter += 1;
  return inter / (A.size + B.size - inter);
};

const packs = fs
  .readdirSync(lessonsDir, { withFileTypes: true })
  .filter((d) => d.isDirectory() && /^PREM-/.test(d.name));

const terms = new Map(); // key: term-name (normalized) -> { canonicalId, uses: [...] }

for (const pack of packs) {
  const lessonPath = path.join(lessonsDir, pack.name, "lesson.json");
  const glossaryPath = path.join(lessonsDir, pack.name, "glossary.json");
  if (!fs.existsSync(lessonPath) || !fs.existsSync(glossaryPath)) continue;
  const lesson = JSON.parse(fs.readFileSync(lessonPath, "utf8"));
  if (lesson.status !== "published") continue; // the ledger describes the live course only
  const glossary = JSON.parse(fs.readFileSync(glossaryPath, "utf8"));
  for (const entry of glossary.terms || glossary.entries || []) {
    const key = String(entry.term || "").trim().toLowerCase();
    if (!key) continue;
    if (!terms.has(key)) terms.set(key, []);
    terms.get(key).push({
      lessonId: lesson.id,
      rank: lessonRank(lesson.id),
      id: entry.id,
      term: entry.term,
      definition: entry.definition,
      aliases: entry.aliases || [],
      alignment: entry.alignment || null,
    });
  }
}

const ledger = [];
for (const [key, uses] of [...terms.entries()].sort()) {
  uses.sort((a, b) => a.rank - b.rank);
  const distinctDefs = new Set(uses.map((u) => normalizeDef(u.definition)));
  let status = "single-lesson";
  if (uses.length > 1) status = distinctDefs.size === 1 ? "consistent" : "divergent";

  // Lowest pairwise overlap across the uses: near 1 means reworded-same-concept, low means the
  // sense genuinely shifts. Only meaningful when status is divergent.
  let minOverlap = 1;
  if (status === "divergent") {
    for (let i = 0; i < uses.length; i += 1)
      for (let j = i + 1; j < uses.length; j += 1)
        minOverlap = Math.min(minOverlap, jaccard(uses[i].definition, uses[j].definition));
  }
  const sense = status !== "divergent" ? null : minOverlap >= 0.5 ? "reworded" : "shifted";

  // For a divergent term, each use after the first should declare an alignment pointing at an
  // earlier lesson. Flag the ones that do not — those are the silent redefinitions.
  const undeclared = [];
  if (status === "divergent") {
    for (const use of uses.slice(1)) {
      const a = use.alignment;
      const declared = a && a.priorLesson && a.relation;
      if (!declared) undeclared.push(use.lessonId);
    }
  }

  ledger.push({
    term: key,
    status,
    sense,
    minDefinitionOverlap: status === "divergent" ? Number(minOverlap.toFixed(2)) : null,
    lessons: uses.map((u) => u.lessonId),
    ids: [...new Set(uses.map((u) => u.id))],
    undeclaredRedefinitions: undeclared,
    uses: uses.map((u) => ({
      lessonId: u.lessonId,
      id: u.id,
      definition: u.definition,
      aliases: u.aliases,
      alignment: u.alignment,
    })),
  });
}

const summary = {
  terms: ledger.length,
  multiLesson: ledger.filter((t) => t.lessons.length > 1).length,
  consistent: ledger.filter((t) => t.status === "consistent").length,
  divergent: ledger.filter((t) => t.status === "divergent").length,
  divergentUndeclared: ledger.filter((t) => t.undeclaredRedefinitions.length > 0).length,
  senseShifted: ledger.filter((t) => t.sense === "shifted").length,
};

const out = {
  schemaVersion: 1,
  format: "embeddedknowledge-terminology-ledger-v1",
  course: "premed",
  purpose:
    "Consult before defining a glossary term. If a prior published lesson already defines it, " +
    "adopt that meaning and cross-reference, or declare a glossary `alignment` block that names " +
    "the prior lesson and the relation (adopt/narrow/extend/distinct-sense/supersede) with a " +
    "rationale. Never silently redefine a term another lesson owns.",
  summary,
  terms: ledger,
};

fs.writeFileSync(outPath, `${JSON.stringify(out, null, 2)}\n`);
console.log(
  `Built terminology ledger: ${summary.terms} terms, ${summary.multiLesson} multi-lesson, ` +
    `${summary.divergent} text-divergent (${summary.senseShifted} sense-shifted), ` +
    `${summary.divergentUndeclared} with no declared alignment.`
);
