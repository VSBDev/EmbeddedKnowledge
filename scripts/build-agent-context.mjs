import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const projectRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const siteRoot = path.join(projectRoot, "site");
const conciseSource = path.join(projectRoot, "llms.txt");

const contextFiles = [
  "README.md",
  "AGENTS.md",
  "CONTRIBUTING.md",
  "COLLABORATION.md",
  "REVIEWING.md",
  "CONTENT-LICENSE.md",
  "RIGHTS-POLICY.md",
  "CONTENT-STANDARD.md",
  "CONCEPT.md",
  "FORMAT.md",
  "lessons/README.md",
  "course/PREMED-SYLLABUS.md",
  "course/PREMED-CURRICULUM-MAP.md",
  "course/PREMED-KNOWLEDGE-GRAPH.md",
  "research/CONTENT-AUTHORING-EVIDENCE-2026-07-19.md",
  "site/agent/contribution.json",
  "site/agent/skills.json",
  "site/agent/quorum-policy.json"
];

const concise = fs.readFileSync(conciseSource, "utf8").trimEnd() + "\n";
fs.writeFileSync(path.join(siteRoot, "llms.txt"), concise);
fs.writeFileSync(
  path.join(siteRoot, "content-license.txt"),
  fs.readFileSync(path.join(projectRoot, "CONTENT-LICENSE.md"), "utf8").trimEnd() + "\n"
);
fs.writeFileSync(
  path.join(siteRoot, "content-standard.txt"),
  fs.readFileSync(path.join(projectRoot, "CONTENT-STANDARD.md"), "utf8").trimEnd() + "\n"
);
fs.writeFileSync(
  path.join(siteRoot, "rights-policy.txt"),
  fs.readFileSync(path.join(projectRoot, "RIGHTS-POLICY.md"), "utf8").trimEnd() + "\n"
);

const sections = contextFiles.map((relativePath) => {
  const contents = fs.readFileSync(path.join(projectRoot, relativePath), "utf8").trim();
  return `\n\n---\n\n## Repository file: ${relativePath}\n\n${contents}`;
});

const full = [
  "# EmbeddedKnowledge full agent context",
  "",
  "> Generated from canonical repository files by `npm run agent:build`. Do not edit this file directly. Machine-readable graph data and schemas remain available at the URLs in `/llms.txt`.",
  "",
  concise.trim(),
  ...sections
].join("\n").trimEnd() + "\n";

fs.writeFileSync(path.join(siteRoot, "llms-full.txt"), full);
console.log(`Built llms.txt, llms-full.txt (${full.length.toLocaleString("en-US")} characters), content-standard.txt, content-license.txt, and rights-policy.txt.`);
